/**
 * Search NAICS codes by code prefix OR title substring.
 * Returns matching codes sorted by relevance:
 *   1. Exact code match
 *   2. Code starts with query
 *   3. Title starts with query (case-insensitive)
 *   4. Title contains query (case-insensitive)
 * Max 50 results.
 */
export function searchNAICS(query) {
  if (!query || !query.trim()) return NAICS_CODES.slice(0, 50)

  const q = query.trim().toLowerCase()

  const exactCode   = []
  const startsCode  = []
  const startsTitle = []
  const containsTitle = []

  for (const entry of NAICS_CODES) {
    const codeL  = entry.code.toLowerCase()
    const titleL = entry.title.toLowerCase()

    if (codeL === q) {
      exactCode.push(entry)
    } else if (codeL.startsWith(q)) {
      startsCode.push(entry)
    } else if (titleL.startsWith(q)) {
      startsTitle.push(entry)
    } else if (titleL.includes(q)) {
      containsTitle.push(entry)
    }
  }

  return [...exactCode, ...startsCode, ...startsTitle, ...containsTitle].slice(0, 50)
}

/**
 * Returns the NAICS entry for an exact code match, or undefined.
 */
export function getNAICSByCode(code) {
  if (!code) return undefined
  return NAICS_CODES.find(e => e.code === String(code))
}

/**
 * Returns the hierarchy chain for a given code.
 * e.g. for '5411' returns [{code:'54',...}, {code:'541',...}, {code:'5411',...}]
 */
export function getNAICSHierarchy(code) {
  if (!code) return []
  const c = String(code)
  const chain = []

  // Build ancestor prefixes (2-digit, 3-digit, 4-digit, 5-digit, 6-digit)
  for (let len = 2; len <= c.length; len++) {
    const prefix = c.slice(0, len)
    const entry = NAICS_CODES.find(e => e.code === prefix)
    if (entry) chain.push(entry)
  }

  return chain
}
